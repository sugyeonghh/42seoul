int	ft_sqrt(int nb)
{
	int a;

	if (nb <= 0)
		return (0);
	else if (nb == 1)
		return (1);
	a = 2;
	while (a < nb / a)
		a++;
	if (a * a == nb)
		return (a);
	return (0);
}
