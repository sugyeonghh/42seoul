#include <unistd.h>

void	ft_print_comb2(void)
{
	char	result[5];
	int		i;
	int		j;

	result[2] = ' ';
	i = 0;
	while (i <= 98)
	{
		j = i + 1;
		result[0] = i / 10 + '0';
		result[1] = i % 10 + '0';
		while (j <= 99)
		{
			result[3] = j / 10 + '0';
			result[4] = j % 10 + '0';
			write(1, result, 5);
			if (!(i == 98 && j == 99))
				write(1, ", ", 2);
			j++;
		}
		i++;
	}
}
