int	ft_str_is_numeric(char *str)
{
	int is_num;
	int i;

	is_num = 1;
	i = 0;
	while (str[i] != '\0')
	{
		if (!(str[i] >= '1' && str[i] <= '9'))
		{
			is_num = 0;
			return (is_num);
		}
		i++;
	}
	if (i == 0)
		is_num = 1;
	return (is_num);
}
