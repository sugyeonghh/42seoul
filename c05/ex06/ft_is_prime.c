int	ft_is_prime(int nb)
{
	int a;

	if (nb <= 1)
		return (0);
	else if (nb == 2)
		return (1);
	a = 3;
	while (a < nb || nb % 2 == 0)
	{
		if (nb % a == 0)
			return (0);
		a++;
	}
	return (1);
}
